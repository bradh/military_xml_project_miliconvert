package org.miliconvert.xsmt.gmodel;

public class Box {

	private String modelId;
	private int x;
	private int y;
	private int w;
	private int h;
	
	public Box(String modelId, int x, int y, int w, int h) {
		super();
		this.modelId = modelId;
		this.x = x;
		this.y = y;
		this.w = w;
		this.h = h;
	}
	public String getModelId() {
		return modelId;
	}
	public void setModelId(String modelId) {
		this.modelId = modelId;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getW() {
		return w;
	}
	public void setW(int w) {
		this.w = w;
	}
	public int getH() {
		return h;
	}
	public void setH(int h) {
		this.h = h;
	}
	
}
